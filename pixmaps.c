/* make a pixmap */
GtkWidget *create_pixmap(GtkWidget *widget, int pixmap_id)
{
   GtkWidget *pixmap;
   GdkColormap *colormap;
   GdkPixmap *gdkpixmap;
   GdkBitmap *mask;
   static char **xpm_ptr = NULL;

   switch (pixmap_id){
   case CHAIN_V_PIXMAP:
      xpm_ptr = chain_v_xpm;
      break;
   case LOCKED_PIXMAP:
      xpm_ptr = locked_xpm;
      break;
   case SLIDE_PIXMAP:
      xpm_ptr = slide_xpm;
      break;
   case CLOSE_PIXMAP:
      xpm_ptr = close_xpm;
      break;
   case ADD_PIXMAP:
      xpm_ptr = add_xpm;
      break;
   case CLONE_PIXMAP:
      xpm_ptr = clone_xpm;
      break;

   case TRANSLATION_PIXMAP:
      xpm_ptr = translation_xpm;
      break;
   case ROTATION_PIXMAP:
      xpm_ptr = rotation_xpm;
      break;
   case SCALE_PIXMAP:
      xpm_ptr = scale_xpm;
      break;
   case TILT_PIXMAP:
      xpm_ptr = tilt_xpm;
      break;

   case ORTHOGRAPHIC_PIXMAP:
      xpm_ptr = orthographic_xpm;
      break;
   case PERSPECTIVE_PIXMAP:
      xpm_ptr = perspective_xpm;
      break;
   case VOLUME_PIXMAP:
      xpm_ptr = volume_xpm;
      break;

   case ADD_PANE_PIXMAP:
      xpm_ptr = add_pane_xpm;
      break;
   case CLONE_PANE_PIXMAP:
      xpm_ptr = clone_pane_xpm;
      break;
   case CLOSE_PANE_PIXMAP:
      xpm_ptr = close_pane_xpm;
      break;
   case ADD_VIEW_PIXMAP:
      xpm_ptr = add_view_xpm;
      break;
   case ADD_TRANSVERSE_VIEW_PIXMAP:
      xpm_ptr = add_transverse_view_xpm;
      break;
   case ADD_SAGITTAL_VIEW_PIXMAP:
      xpm_ptr = add_sagittal_view_xpm;
      break;
   case ADD_CORONAL_VIEW_PIXMAP:
      xpm_ptr = add_coronal_view_xpm;
      break;
   case CLONE_VIEW_PIXMAP:
      xpm_ptr = clone_view_xpm;
      break;
   case CLOSE_VIEW_PIXMAP:
      xpm_ptr = close_view_xpm;
      break;

   default:
      g_error("create_pixmap passed non-existant pixmap id %d\n", pixmap_id);
      }

   colormap = gtk_widget_get_colormap(widget);
   gdkpixmap = gdk_pixmap_colormap_create_from_xpm_d(NULL, colormap, &mask,
                                                     NULL, xpm_ptr);

   pixmap = gtk_pixmap_new(gdkpixmap, mask);
   gdk_pixmap_unref(gdkpixmap);
   gdk_bitmap_unref(mask);
   return pixmap;
   }
